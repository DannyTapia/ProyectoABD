    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try{
           if(lbl_nomdep.getText().equals("")||(lbl_nom.getText().equals(""))||(lbl_apat.getText().equals(""))||(lbl_amat.getText().equals(""))){
            javax.swing.JOptionPane.showMessageDialog(this, "Debes llenar todos los campos","AVISO!",javax.swing.JOptionPane.INFORMATION_MESSAGE);
            lbl_nomdep.requestFocus();
            }
           else{
           Class.forName("com.mysql.jdbc.Driver");
           Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/udg","root","");
           PreparedStatement pst= con.prepareStatement("INSERT INTO departamentos (NomDepa,nombre,apaterno,amaterno,nomimg,img) VALUES(?,?,?,?,?,?)");
           //PreparedStatement pps=cn.prepareStatement("INSERT INTO calis (NControl,contrase√±a) VALUES(?,?)");
           FileInputStream archivofoto;
           pst.setString(1,lbl_nomdep.getText());
           pst.setString(2,lbl_nom.getText());
           pst.setString(3,lbl_apat.getText());
           pst.setString(4,lbl_amat.getText());
           pst.setString(5,txt_ruta.getText());
           archivofoto=new FileInputStream(txt_ruta.getText());
           pst.setBinaryStream(6, archivofoto);
           pst.executeUpdate();
           //ResultSet rs=pst.executeUpdate()
               JOptionPane.showMessageDialog(null,"Se guardaron los datos correctamente");
               MAlumno p1= new MAlumno();
               p1.setVisible(true);
               this.dispose();
               lbl_nomdep.setText("");
               lbl_nom.setText("");
               lbl_apat.setText("");
               lbl_amat.setText("");
               txt_ruta.setText("");
               
           }
           
       }
       catch (Exception e){
           JOptionPane.showMessageDialog(null, e);
       }
    }//GEN-LAST:event_jButton1ActionPerformed

